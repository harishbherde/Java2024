public class UnaryOperator {
    public static void main(String [] args)
    {
        int x = 50;
        System.out.println(x++); //50
        System.out.println(++x); //52

        System.out.println(++x); //53
        System.out.println(x); //53
        System.out.println(x++); //53 


    }
    
}
